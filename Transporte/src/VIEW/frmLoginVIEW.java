    private void logar() {
        try {
            int cpf_usuario = txtCpf_usuario.getText();
            String username = txtUsername.getText();
            String senha = txtSenha.getText();
            String nome_aluno = txtNome_aluno.getText();
            String turma = txtTurma.getText();
            String escola = txtEscola.getText();
            String turno = txtTurno.getText();

            UsuarioDTO objusuariodto = new UsuarioDTO();
            objusuariodto.setCpf_usuario(cpf_usuario);
            objusuariodto.setUsername(username);
            objusuariodto.setSenha(senha);
            objusuariodto.setNome_aluno(nome_aluno);
            objusuariodto.setTurma(turma);
            objusuariodto.setEscola(escola);
            objusuariodto.setTurno(turno);

            UsuarioDAO objusuariodao = new UsuarioDAO();
            ResultSet rsusuariodao = objusuariodao.autenticacaousuario(objusuariodto);

            if (rsusuariodao.next()) {
                frmHomeVIEW objfrmHomeVIEW = new frmHomeVIEW();
                objfrmHomeVIEW.setVisible(true);
            } else {
                JOptionPane.showMessageDialog(null, "Esse usu√°rio j√° existe");
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "FRMLOGINVIEW" + e);
        }

    }
